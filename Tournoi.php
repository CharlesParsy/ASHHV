<h2>Tournoi Internationnal René Pruvot</h2>

<br>
<p>La 4éme édition du Tournoi Internationnal René Pruvot aura lieu le samedi 18 et dimanche 19 mai 2024 au Complexe sportif Vauban de Valenciennes.</p>
<br>
<p>Ce tournoi handisportif 2024 doit être un moment unique de plaisir, de convivialité et d'échanges permettant de tisser des relations d'amitié entre citoyens européens et de valoriser chacun des participents.<br>
Il s'inscrit dans un contexte Olympique inédit en France.<br>
Nous souhaitons que tous, équipes, joueurs, encadrants, spectateurs, respectent les valeurs olympiques qui sont l'excellence, le respect et l'amitié ainsi que la philosophie du mouvement olympique.<br>
Le mouvement olympique a pour but de contribuer à batir un monde pacifique et meilleur en éduquant la jeunesse par le moyen du sport pratiqué sans discrimination d'aucune sorte et dans l'esprit olympique qui exige la compréhension mutuelle, l'esprit d'amitié, la solidarité et le fair-play.</p>
<h3>Equipes</h3>
<p>10 équipes dont 4 françaises (ASHHV, Capelle La Grande, CAPSAA et Stade Rochelais), 3 équipes belges (BHBB, RBBC et Rolling Chair), 2 équipes italiennes (Seregno et Trento) et 1 équipe turc (Samsun) s'affronteront.</p>

<h3>Concert</h3>
<p>Pour l'occasion, le groupe Appetite (Tribute Guns N'Roses) viendra jouer le samedi 18 mai.</p>

